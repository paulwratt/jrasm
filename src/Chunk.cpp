//-----------------------------------------------------------------------------
// ChunkOwner
//-----------------------------------------------------------------------------
ChunkOwner::~ChunkOwner()
{
	Clear();
}

void ChunkOwner::Clear()
{
	for (auto pChunk : *this) {
		Chunk::Delete(pChunk);
	}
	clear();
}

