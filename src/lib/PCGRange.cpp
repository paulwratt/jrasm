//-----------------------------------------------------------------------------
// PCGRangeOwner
//-----------------------------------------------------------------------------
PCGRangeOwner::~PCGRangeOwner()
{
	Clear();
}

void PCGRangeOwner::Clear()
{
	for (auto pPCGRange : *this) {
		delete pPCGRange;
	}
	clear();
}
