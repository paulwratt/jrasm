String ToLower(const char *str)
{
	String rtn;
	for (const char *p = str; *p != '\0'; ) {
		char ch = *p;
		if ('A' <= *p && *p <= 'Z') {
			rtn += *p - 'A' + 'a';
			p++;
		} else {
			rtn += *p;
			p++;
		}
	}
	return rtn;
}
